package h04;

public interface WithNaturalCoordinates {

    /**
     * @return the x-coordiante
     */
    int getX();

    /**
     * @return the y-coordinate
     */
    int getY();

    /**
     * Sets the x-coordinate to the given Parameter
     * @param x to set the x-coordinate
     */
    void setX(int x);

    /**
     * Sets the y-coordinate to the given Parameter
     * @param y to set the y-coordiante
     */
    void setY(int y);
}
